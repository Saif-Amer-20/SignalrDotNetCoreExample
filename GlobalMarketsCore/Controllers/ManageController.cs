        public List<ApplicationUser> GetJsonData(int? page, int? limit, string sortBy, string direction, out int total, string userName = null,
            string email = null, string firstName = null, string lastName = null, string roleId = null, string country = null)
        {
            var records = _userManager.Users
                .Select(c => new ApplicationUser()
                {
                    UserName = c.UserName,
                    Email = c.Email,
                    FirstName = c.FirstName,
                    LastName = c.LastName,
                    Country = c.Country,
                    Id = c.Id

                })
                
                .ToArray().AsQueryable();
            if (!string.IsNullOrEmpty(roleId))
            {
                var users = _userManager.GetUsersInRoleAsync(roleId).Result;
                records = records.Where(r => users.Any(c => c.Id == r.Id));
            }

            if (!string.IsNullOrEmpty(country))
            {
                records = records.Where(r => r.Country == country);
            }
         
            if (!string.IsNullOrEmpty(userName))
            {
                records = records.Where(r => r.UserName.ToLower().Contains(userName.ToLower()));
            }
            if (!string.IsNullOrEmpty(email))
            {
                records = records.Where(r => r.Email.ToLower().Contains(email.ToLower()));
            }
            if (!string.IsNullOrEmpty(firstName))
            {
                records = records.Where(r => r.FirstName.ToLower().Contains(firstName.ToLower()));
            }
            if (!string.IsNullOrEmpty(lastName))
            {
                records = records.Where(r => r.LastName.ToLower().Contains(lastName.ToLower()));
            }


            total = records.Count();

            if (!string.IsNullOrEmpty(sortBy) && !string.IsNullOrEmpty(direction))
            {
                if (direction.Trim().ToLower() == "asc")
                {
                    records = SortHelper.OrderBy(records, sortBy);
                }
                else
                {
                    records = SortHelper.OrderByDescending(records, sortBy);
                }
            }
            if (page.HasValue && limit.HasValue)
            {
                int start = (page.Value - 1) * limit.Value;
                records = records.Skip(start).Take(limit.Value);
            }

            return records.ToList();
        }


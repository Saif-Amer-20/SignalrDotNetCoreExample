            return records.ToList();
        }


        [HttpPost]
        public async Task<IActionResult> AddRole(string roleId, string userId)
        {
            var appUser = await _userManager.Users.FirstOrDefaultAsync(u => u.Id == userId);
            var isInRole = await _userManager.IsInRoleAsync(appUser, roleId);
            if (isInRole)
            {
                return Json("User already have the selected role");
            }
            else
            {
                await _userManager.AddToRoleAsync(appUser, roleId);
                return Json("Ok");
            }
        }

        [HttpPost]
        public async Task<IActionResult> RemoveRole(string roleId, string userId)
        {
            var appUser = await _userManager.FindByIdAsync(userId);
            if (appUser == null)
            {
                return Json("User Not Found");
            }

            var result = await _userManager.RemoveFromRoleAsync(appUser, roleId);
            if (result.Succeeded)
            {
                return Json(appUser.UserName + " is successfully un assigned from role " + roleId);
            }

            return Json("An error has occured, please return back to system administrator");
        }

